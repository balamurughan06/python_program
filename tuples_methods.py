#count()
print("--- count() ---")
thisTuple = (1, 2, 3, 1, 2, 1)
print(thisTuple.count(1)) #count of 1
print(thisTuple.count(4)) #count of 4

#index()
print("--- index() ---")
thisTuple = (1, 2, 3)
print(thisTuple.index(2)) #index of 2
