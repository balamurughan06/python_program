print(bool("Hello")) # True And non-empty string is True
print(bool(15)) # True And non-zero number is True
print(bool("")) # False And empty string is False
print(bool(0)) # False And zero is False
print(bool(None)) # False And None is False
print(bool([])) # False And empty list is False
print(bool({})) # False And empty dictionary is False
print(bool(())) # False And empty tuple is False